void main()
{
    // strings
    print("Hello Lauryn!");

    var name = "Lauryn";
    var age = 22;

    print("My name is ${name} and I am ${age} years old.");

    // operators
    int a = 2;
    int c = 6;

    int d = a*c;

    print("$a * $c = $d");

    // loops
    print("\n");
    print("from for loop");
    for(int i = 10; i > 0; i--)
    {
        print("Laurynnnn");
    }

    print("\n");
    print("from while loop");
    int i = 0;
    while(i<10)
    {
        print("Laurynnnn");
        i++;
    }

}

