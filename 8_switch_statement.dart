void main(List<String> args){

    int mark = 50;

    switch(mark){
        case 0:
        print("Grade for $mark is F");
        break;

        case 50:
        print("Grade for $mark is D");
        break;

        case 100:
        print("Grade for $mark is A");
        break;

        default:
        print("Unknown mark");
        break;

    }
}